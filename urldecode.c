int urldecode(unsigned char *low) {
	register unsigned char *high = low;
	register unsigned char character;
	while (*high) {
		if (*high == '%') {
			// Check for premature string end
			if (!*++high || !*(high+1)) {
				errno = EINVAL;
				return -1;
			}

			character = hexChar[*high] * 0x10;
			character += hexChar[*++high];

			// Reject illegal characters
			if (character <= 0x1F) {
				errno = EINVAL;
				return -1;
			}

			*low = character;
		}
		else
			*low = *high;
		high++;
		low++;
	}

	*low = '\0';
	return 0;
}
